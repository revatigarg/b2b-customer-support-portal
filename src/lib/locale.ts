// For backward compatibility
export const LOCALE_CONFIGS = MARKET_CONFIGS;

export function formatCurrency(amount: number, market: MarketCode): string {
  const config = MARKET_CONFIGS[market];
  const formatted = amount.toLocaleString('en-US', {
    minimumFractionDigits: 2,
    maximumFractionDigits: 2,
  });
  
  return config.currency.position === 'before'
    ? `${config.currency.symbol}${formatted}`
    : `${formatted} ${config.currency.symbol}`;
}

export function formatDate(date: Date, market: MarketCode): string {
  return date.toLocaleDateString('en-US');
}
